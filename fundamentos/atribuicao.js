const a = 7 // = ' atribuição'
let b = 3

b += a // b = b + a "atribuião aditiva"
console.log(b)

b -= 4 // b = b -4 " atribuição subtrativa"
console.log(b)

b *= 2 // b = b * 2 "atribuição mutiplicativa"
console.log(b)

b /= 2 // b = b / 2 " atribuição divisiva"
console.log(b)

b %= 2 // b = % 2 " atribuição modular"
console.log(b)